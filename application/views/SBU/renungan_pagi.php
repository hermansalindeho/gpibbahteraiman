            <p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                "Setiap orang bisa menjadi guru bagi siapapun". Ungkapan yang bermakna seseorang bisa memberikan keteladanan kepada siapa saja. Tidak hanya keteladanan dalam kebaikan hidup namun mengajarkan tentang kebenaran. Kebenaran dalam hal keyakinan, kebenaran setiap tindakan atau kebenaran akan sesuatu hal.
            </p>
            
           	<p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                Surat Paulus kepada Timotius menekankan harapan supaya tetap menyampaikan kebenaran tentang Allah dengan kasih, tekun dan tulus (ayat 5). Paulus melihat banyak umat yang "sibuk" dengan "dongeng dan silsilah yang tiada putus-putusnya, yang hanya menghasilkan persoalan belaka, dan bukan tertib hidup keselamatan yang diberikan Allah dalam iman" (ayat 4). Lebih parah Paulus melihat adanya pengajar tentang hukum Taurat namun tidak mengerti apa yang disampaikan, "menjadi pengajar hukum Taurat tanpa mengerti perkataan mereka sendiri dan pokok-pokok yang secara mutlak mereka kemukakan" (ayat 7). Sesuatu yang disampaikan atau diajarkan tidak dipahami. Celakanya tidak sesuai dengan perbuatan. Sesungguhnya setiap pengajaran kebenaran - khususnya kebenaran akan Tuhan, dimengerti dan dipahami serta segala perbuatan sesuai dengan kebenaran yang diajarkan. Keprihatinan Paulus ini sangat penting karena dapat mengganggu kehidupan dan kesalehan umat. Bagi Paulus hukum "Taurat" Allah sangat baik jika tapat digunakan (ayat 8).
            </p>

            <p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                Harapan Paulus kepada Timotius juga berlaku bagi kita. Di tengah kondisi dan situasi saat ini dengan berbagai macam keterbukaan informasi, kita dituntut untuk tetap menyampaikan kebenaran firman Tuhan. Menyampaikan ajaran kebenaran dalam sikap, pikiran, tutur kata dan perilaku. Kita harus menyadari bahwa diri kita merupakan media pengajaran dalam menyampaikan kebenaran firman-Nya. Jadi, pakailah diri kita untuk menyampaikan kebenaran Tuhan saat ini. Jangan menjadikan diri kita batu sandungan atas kebenaran Tuhan.
            </p>

