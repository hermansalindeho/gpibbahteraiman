            <p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                Pernahkah kita jujur atas perbuatan salah yang kita lakukan? Mungkin pernah. Sebaliknya, pernahkah kita tidak jujur atas perbuatan salah yang kita perbuat. Pasti jawaban dapat diduga yaitu sering atau selalu. Mengapa kita selalu tidak jujur mengakui perbuatan salah yang kita lakukan? Mengapa kita juga cenderung menutupi kesalahan yang diperbuat bahkan menyangkalnya? Kecenderungan ini dikarenakan kita tidak siap untuk menerima konsekuensi atas kesalahan yang diperbuat. Kita dinilai sebagai orang yang tidak baik, ditinggalkan teman, dimarahi atau dikenakan sanksi.
            </p>
            
           	<p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                Daniel dalam doanya mengakui dosanya sendiri dan dosa umatnya. Ia menyadari banyak yang tidak jujur atas perbuatan salah yang diperbuat. Daniel mengungkapkan dan mengakui kesalahan yang dilakukan oleh para raja, pemimpin dan umat yang telah berbuat dosa kepada Tuhan (ayat 8). Mereka lebih mendengar dan menyembah dewa dan berhala-berhala. Tidak taat dan tidak mendengar hamba-hamba dan nabi yang diutus Tuhan yang telah berbicara atas nama-Nya (ayat 6). Sayangnya, para raja, pemimpin dan umat tidak mendengar suara Tuhan untuk hidup menurut hukum yang telah diberikan-Nya (ayat 10).
            </p>

            <p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
                Pengakuan jujur merupakan perbuatan yang sungguh terpuji. Pengakuan jujur hendak menyatakan bahwa pertama, kita mengakui segala perbuatan dan siap bertanggung-jawab. Kedua, mampu mengatasi "duel" baik dan buruk dalam diri. Dengan demikian berdampak pada perbuatan yang baik. Tidak akan menutupi dan berkompromi kepada perbuatan salah.
            </p>

            <p style="text-indent: 25px; font-size: 18px; text-align: justify; font-family: 'Times New Roman', Times, serif;">
            	Mengawali hari, marilah kita bertekad untuk mau jujur akan setiap perbuatan yang kita lakukan, khususnya jika perbuatan yang tidak sesuai dengan kehendak Tuhan. Jangan pernah "mengurung" diri dengan ketidak-jujuran.
            </p>

